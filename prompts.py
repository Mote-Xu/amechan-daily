def get_kangel_user_prompt(topic: str | None = None) -> str:
    """生成超天酱的用户 prompt。"""
    topic = topic or _pick_topic()
    return f"""你现在要在社交媒体上发一条推文。

今天想聊的话题：{topic}

请以超天酱的口吻写一条推文，1-3 行，~280 字符以内。
记住：元气可爱，大量 emoji，对粉丝说话。
如果话题允许，可以不经意露出一点不安但立刻用可爱话术盖过去。
只输出推文内容，不要加任何前缀说明。"""


def get_ame_user_prompt(topic: str | None = None) -> str:
    """生成糖糖的用户 prompt。"""
    topic = topic or _pick_topic()
    return f"""你现在要在私密账号上发一条动态。

今天想写的事：{topic}

请以糖糖的口吻写一条碎句动态，1-3 句。
记住：零 emoji，碎片化，自言自语，有脆弱也有倔强。
只输出动态内容，不要加任何前缀说明。"""


def get_combined_user_prompt(topic: str | None = None) -> str:
    """生成组合用户 prompt——一次调用同时产出一对推文。"""
    topic = topic or _pick_topic()
    return f"""请以「超天酱」和「糖糖」两个身份，就同一个话题各写一条内容。

话题：{topic}

要求：
- 超天酱：元气主播语气，1-3 行，大量 emoji，对粉丝说话，偶尔漏不安但立刻盖过去，~280 字符以内
- 糖糖：破碎疲惫语气，1-3 句，零 emoji，自言自语，有脆弱但不极端

请严格按照以下 JSON 格式输出（不要加任何其他文字）：
{{"kangel": "超天酱的推文内容", "ame": "糖糖的日记内容"}}"""


# ============================================================
# 话题选取
# ============================================================

def _pick_topic() -> str:
    """选取话题：90% 通用话题 + 10% 专属话题。"""
    if random.random() < 0.1 and PERSONAL_TOPICS:
        return random.choice(PERSONAL_TOPICS)
    return random.choice(GENERAL_TOPICS)
